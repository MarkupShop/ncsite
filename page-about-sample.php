<section class="page-section bg-gray">

	<div class="wrapper">

		<h3 class="content-title">Goals &amp; Mission</h3>

		<p>NCSITE was established to promote sound transportation engineering practices in the planning, design, 
		operation, and management of transportation facilities and equipment for the safe, convenient, and efficient 
		movement of people and goods. To achieve this goal, NCSITE has adopted the following objectives:</p>

		<ul>
			<li>Encourage qualified persons to enter the transportation engineering profession.</li>
			<li>Encourage professional training and development of persons engaged in transportation engineering.</li>
			<li>Develop increased coordination and cooperation with local governing bodies, private organizations, 
			professional engineering groups, and ITE.</li>
			<li>Increase public awareness and involvement in transportation engineering activities.</li>
			<li>Stimulate transportation-related research activities.</li>
			<li>Promote communication between persons with mutual interests in transportation engineering.</li>
			<li>Establish an exchange of professional information and maintain a centralized point of reference and 
			action for transportation engineering professionals.</li>
			<li>Represent the interests of members in the transportation engineering profession when 
			transportation-related legislation and policy matters are represented.</li>
		</ul>

	</div>

</section>

